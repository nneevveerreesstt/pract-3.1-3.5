def bubble_sort_detailed(arr):
    """
    –í–µ—Ä—Å–∏—è —Å –ø–æ–¥—Ä–æ–±–Ω—ã–º –≤—ã–≤–æ–¥–æ–º –∫–∞–∂–¥–æ–≥–æ —à–∞–≥–∞ –¥–ª—è –æ–±—É—á–µ–Ω–∏—è
    """
    n = len(arr)
    print(f"–ù–∞—á–∞–ª—å–Ω—ã–π –º–∞—Å—Å–∏–≤: {arr}")
    print(f"–î–ª–∏–Ω–∞ –º–∞—Å—Å–∏–≤–∞: {n}")
    print("-" * 60)
    
    for i in range(n):
        print(f"\nüéØ –ò–¢–ï–†–ê–¶–ò–Ø {i + 1}:")
        print(f"   –ë—É–¥–µ–º –ø—Ä–æ—Å–º–∞—Ç—Ä–∏–≤–∞—Ç—å —ç–ª–µ–º–µ–Ω—Ç—ã —Å 0 –¥–æ {n - i - 1}")
        
        swapped = False
        comparisons = 0  # –°—á–µ—Ç—á–∏–∫ —Å—Ä–∞–≤–Ω–µ–Ω–∏–π
        swaps = 0        # –°—á–µ—Ç—á–∏–∫ –æ–±–º–µ–Ω–æ–≤
        
        for j in range(0, n - i - 1):
            comparisons += 1
            print(f"   –°—Ä–∞–≤–Ω–∏–≤–∞–µ–º arr[{j}]={arr[j]} –∏ arr[{j + 1}]={arr[j + 1]}", end="")
            
            if arr[j] > arr[j + 1]:
                # –ú–µ–Ω—è–µ–º —ç–ª–µ–º–µ–Ω—Ç—ã –º–µ—Å—Ç–∞–º–∏
                arr[j], arr[j + 1] = arr[j + 1], arr[j]
                swapped = True
                swaps += 1
                print(f" ‚Üí –û–ë–ú–ï–ù! –¢–µ–ø–µ—Ä—å: {arr}")
            else:
                print(f" ‚Üí –ø–æ—Ä—è–¥–æ–∫ –≤–µ—Ä–Ω—ã–π")
        
        print(f"   –ò—Ç–æ–≥ –∏—Ç–µ—Ä–∞—Ü–∏–∏ {i + 1}: {arr}")
        print(f"   –°—Ä–∞–≤–Ω–µ–Ω–∏–π: {comparisons}, –û–±–º–µ–Ω–æ–≤: {swaps}")
        
        if not swapped:
            print(f"   ‚≠ê –ù–µ –±—ã–ª–æ –æ–±–º–µ–Ω–æ–≤! –ú–∞—Å—Å–∏–≤ –æ—Ç—Å–æ—Ä—Ç–∏—Ä–æ–≤–∞–Ω –ø–æ—Å–ª–µ {i + 1} –∏—Ç–µ—Ä–∞—Ü–∏–π")
            break
        else:
            print(f"   –ü–æ—Å–ª–µ–¥–Ω–∏–µ {i + 1} —ç–ª–µ–º–µ–Ω—Ç(–æ–≤) –Ω–∞ —Å–≤–æ–∏—Ö –º–µ—Å—Ç–∞—Ö")
    
    print("-" * 60)
    print(f"–§–∏–Ω–∞–ª—å–Ω—ã–π —Ä–µ–∑—É–ª—å—Ç–∞—Ç: {arr}")
    return arr

