def binary_search(arr, target):
    """
    Бинарный поиск в отсортированном массиве
    Возвращает индекс элемента target в массиве arr
    Если элемент не найден, возвращает -1
    """
    # Левая граница поиска (начало массива)
    left = 0
    # Правая граница поиска (конец массива)
    right = len(arr) - 1
    
    # Пока границы не пересеклись
    while left <= right:
        # Находим средний индекс между left и right
        mid = (left + right) // 2
        
        # Если средний элемент равен целевому - нашли!
        if arr[mid] == target:
            return mid
        
        # Если целевой элемент меньше среднего, ищем в левой половине
        elif arr[mid] > target:
            right = mid - 1  # Сдвигаем правую границу
        
        # Если целевой элемент больше среднего, ищем в правой половине
        else:
            left = mid + 1   # Сдвигаем левую границу
    
    # Элемент не найден
    return -1

